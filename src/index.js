/**
 * Adds two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
export var add = function (a, b) { return a + b; };
/**
 * Substracts two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
export var subtract = function (a, b) { return a - b; };
/**
 * Multiplies two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
export var multiply = function (a, b) { return a * b; };
